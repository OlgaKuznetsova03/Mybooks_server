def env_bool(*names: str, default: bool = False) -> bool:
    if not names:
        raise ValueError("At least one environment variable name must be provided")

    for name in names:
        value = os.getenv(name)
        if value is not None:
            return value.lower() in {"1", "true", "yes"}

    return default


def env_int(*names: str, default: int = 0, min_value: int | None = None) -> int:
    if not names:
        raise ValueError("At least one environment variable name must be provided")

    for name in names:
        value = os.getenv(name)
        if value is None:
            continue

        try:
            parsed = int(value)
        except (TypeError, ValueError):
            print(
                f"⚠️  Invalid integer for {name}={value!r} – using default {default}",
            )
            return max(default, min_value or default)

        if min_value is not None and parsed < min_value:
            print(
                f"⚠️  {name} is below the minimum of {min_value}; "
                f"using {max(default, min_value)} instead",
            )
            return max(default, min_value)

        return parsed

    return default


def env_decimal(
    *names: str,
    default: str | Decimal = "0",
    min_value: Decimal | None = None,
) -> Decimal:
    if not names:
        raise ValueError("At least one environment variable name must be provided")

    if isinstance(default, Decimal):
        default_decimal = default
    else:
        default_decimal = Decimal(str(default))

    for name in names:
        value = os.getenv(name)
        if value is None or value == "":
            continue
        try:
            parsed = Decimal(value)
        except (InvalidOperation, ValueError):
            print(
                f"⚠️  Invalid decimal for {name}={value!r} – using default {default_decimal}",
            )
            return default_decimal

        if min_value is not None and parsed < min_value:
            print(
                f"⚠️  {name} is below the minimum of {min_value}; using {min_value}",
            )
            return max(default_decimal, min_value)

        return parsed

    return default_decimal


